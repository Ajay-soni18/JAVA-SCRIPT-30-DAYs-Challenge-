function adder (num1 , num2){
    console.log(num1+num2);
}
function substrate(num1 , num2){
    console.log(num1-num2);
}
function multiplier(num1 , num2){
    console.log(num1*num2);
}
function divide(num1,num2){
    let result = num1/num2;
    console.log(result);
}
function remainder(num1,num2){
    let result = num1%num2;
    console.log(result);
}

adder(1,2);
substrate(1,2);
multiplier(1,2);
